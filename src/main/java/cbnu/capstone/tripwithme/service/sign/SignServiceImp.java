package cbnu.capstone.tripwithme.service.sign;


import cbnu.capstone.tripwithme.dto.userInfo.UserInfoDTO;
import cbnu.capstone.tripwithme.mapper.user.UserInfoMapper;
import lombok.RequiredArgsConstructor;
import org.springframework.stereotype.Service;

@Service
@RequiredArgsConstructor
public class SignServiceImp implements SignService{

    private final UserInfoMapper userInfoMapper;


    @Override
    public String getUserInfo(String userId) {
        return userInfoMapper.getUserInfo(userId);
    }

    @Override
    public void setUserInfo(String userId, String password, String flag) {
        userInfoMapper.setUserInfo(userId,password,flag);
    }


}
